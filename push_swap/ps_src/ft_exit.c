#include "../inc/push_swap.h"

void			ft_exit(t_push **push)
{
	(*push != NULL) ? clear_list(push) : 0;
	write(1, "\033[31;1m", ft_strlen("\033[31;1m"));
	write(1, "Error\n", 6);
	write(1, "\033[0;0m", ft_strlen("\033[0;0m"));
}
